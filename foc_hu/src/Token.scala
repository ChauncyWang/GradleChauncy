	val SUB = Value(40, "-")
	val STAR = Value(41, "*")
	val SLASH = Value(42, "/")
	val EQ = Value(43, "=")
	val GT = Value(44, ">")
	val LT = Value(45, "<")
	val LPAREN = Value(46, "(")
	val RPAREN = Value(47, ")")
	val LBRACKET = Value(48, "[")
	val RBRACKET = Value(49, "]")
	val COLON = Value(50, ":")
	val DOT = Value(51, ".")
	val SEMI = Value(52, ";")
	val COMMA = Value(53, ",")
	//双界符号
	val NEQ = Value(54, "<>")
	val LTEQ = Value(55, "<=")
	val GTEQ = Value(56, ">=")
	val ASS = Value(57, ":=")
	val DDOT = Value(58, "..")
	val LNOTE = Value(59, "/*")
	val RNOTE = Value(60, "*/")
}
